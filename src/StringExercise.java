public class StringExercise {
    public static void main(String[] args) {
        String pinkFlyod = "We don't need no education\nWe don't need no thought control";
        System.out.println(pinkFlyod);

        String check = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(check);

        String window = "In windows, the main drive is usually C:\\";
        System.out.println(window);

        String backLash = "I can do backslashes \\, double backslashes \\\\, and the amazing triple backslash \\\\\\!";
        System.out.println(backLash);
    }
}
